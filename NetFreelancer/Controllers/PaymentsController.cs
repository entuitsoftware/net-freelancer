                // Transaction Details
                m_payment_id = "8d00bf49-e979-4004-228c-08d452b86380",
                amount = 30,
                item_name = "Once off option",
                item_description = "Some details about the once off payment",

                // Transaction Options
                email_confirmation = true,
                confirmation_address = "sbtu01@payfast.co.za"
